package lesson3;

public class Conditions {

    public static void main(String[] args) {
        // Логічні операції
        int x = 5;
        int y = 4;

        //Логічна операція if
        if (x > y) {
            System.out.println("x > y");
        } else if (x == y) {
            System.out.println("x = y");
        }
        else {
            System.out.println("y > x");
        }
    }

}
